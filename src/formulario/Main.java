    private void jButton2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MousePressed
        // TODO add your handling code here:
        ArrayList<Datos> empleados = new ArrayList<>();
        Datos datos = new Datos();

        if (noHayVacios()) {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios!");
        } else {
            datos.setNombre_completo(etNombreCompleto.getText());
            datos.setNit(etNit.getText());
            datos.setPlaca(etPlaca.getText());
            datos.setMarca(etMarca.getText());
            datos.setModelo(etModelo.getText());
            datos.setAnio(Integer.parseInt(etAnio.getText()));
            datos.setFecha_ingreso(etFechaIngreso.getText());
            datos.setHora_ingreso(etHoraIngreso.getText());
            datos.setFecha_salida(etFechaSalida.getText());
            datos.setHora_salida(etHoraSalida.getText());

            if (rbCarro.isSelected()) {
                datos.setMonto((Integer.parseInt(etHoraSalida.getText().split(":")[0]) - Integer.parseInt(etHoraIngreso.getText().split(":")[0])) * 10);
            } else {
                datos.setMonto((Integer.parseInt(etHoraSalida.getText().split(":")[0]) - Integer.parseInt(etHoraIngreso.getText().split(":")[0])) * 5);
            }

            if (existe()) {
                OperacionArchivo.aniadirArchivo(datos);
            } else {
                OperacionArchivo.crearArchivo(datos);
            }

            limpiar();
            cargarTabla1();
        }
    }//GEN-LAST:event_jButton2MousePressed

